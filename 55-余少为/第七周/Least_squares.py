
# 样本数据
X = [1, 2, 3, 4]
Y = [6, 5, 7, 10]

# 初始化，公式需要的计算元素
s1 = 0
s2 = 0
s3 = 0
s4 = 0
n = len(X)

# 参照公式，凑齐需要的计算元素
for i in range(n):
    s1 += X[i] * Y[i]   # X*Y，求和
    s2 += X[i]          # X的和
    s3 += Y[i]          # Y的和
    s4 += X[i] * X[i]   # X的平方，求和

# 套用公式，计算斜率和截距
k = (n * s1 - s2 * s3) / (n * s4 - s2 * s2)
b = (s3 - k * s2) / n

# 打印公式
print(f'y = {k}x + {b}')
